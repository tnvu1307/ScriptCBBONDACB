FUNCTION fn_txPreAppCheck(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    v_currdate  date;
    v_COMPANYCD varchar2(50);
    v_count     number;
BEGIN
   plog.setbeginsection (pkgctx, 'fn_txPreAppCheck');
   plog.debug(pkgctx,'BEGIN OF fn_txPreAppCheck');
   /***************************************************************************************************
    * PUT YOUR SPECIFIC RULE HERE, FOR EXAMPLE:
    * IF NOT <<YOUR BIZ CONDITION>> THEN
    *    p_err_code := '<<ERRNUM>>'; -- Pre-defined in DEFERROR table
    *    plog.setendsection (pkgctx, 'fn_txPreAppCheck');
    *    RETURN errnums.C_BIZ_RULE_INVALID;
    * END IF;
    ***************************************************************************************************/

    v_currdate := getcurrdate;
    v_COMPANYCD := FN_GET_COMPANYCD;

    if p_txmsg.txfields('25').value <> TO_CHAR(v_currdate, 'DD/MM/RRRR') then
        p_err_code := '-150008';
        plog.setendsection (pkgctx, 'fn_txPreAppCheck');
        RETURN errnums.C_BIZ_RULE_INVALID;
    end if;

    if substr2(p_txmsg.txfields('88').value,0,3) <> v_COMPANYCD then
        p_err_code := '-150006';
        plog.setendsection (pkgctx, 'fn_txPreAppCheck');
        RETURN errnums.C_BIZ_RULE_INVALID;
    end if;

    if substr2(p_txmsg.txfields('88').value,4,1) not in ('P','C','E','F','A','B') then
        p_err_code := '-150006';
        plog.setendsection (pkgctx, 'fn_txPreAppCheck');
        RETURN errnums.C_BIZ_RULE_INVALID;
    end if;

    SELECT count(*) into v_count
    FROM TLLOG T
    LEFT JOIN TLLOGFLD T1 ON T.TXNUM = T1.TXNUM
    WHERE T.TXNUM = p_txmsg.txnum AND TXSTATUS = '4';

    if v_count <= 0 then
        SELECT count(*) into v_count
        FROM VSD_MT518_INF
        WHERE VSDORDERID = p_txmsg.txfields('22').value
        AND STATUSTRANSACTION in ('C','R') AND CONFREQSTATUS IN ('F','A');

        if v_count > 0 then
            p_err_code := '-150030';
            plog.setendsection (pkgctx, 'fn_txPreAppCheck');
            RETURN errnums.C_BIZ_RULE_INVALID;
        end if;
    end if;

    plog.debug (pkgctx, '<<END OF fn_txPreAppCheck');
    plog.setendsection (pkgctx, 'fn_txPreAppCheck');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
      plog.setendsection (pkgctx, 'fn_txPreAppCheck');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txPreAppCheck;

